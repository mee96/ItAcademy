public class Main {
    static void myMethod(String fname, int age) {
        System.out.println(fname + " is " + age);
    }

    public static void main(String[] args) {
        myMethod("Carme", 29);
        myMethod("Victoria", 20);
        myMethod("Rodney", 26);
    }
}


